package com.notif.ai.data

import android.content.Context
import androidx.datastore.core.DataStore
import androidx.datastore.preferences.core.Preferences
import androidx.datastore.preferences.core.booleanPreferencesKey
import androidx.datastore.preferences.core.edit
import androidx.datastore.preferences.core.longPreferencesKey
import androidx.datastore.preferences.preferencesDataStore
import kotlinx.coroutines.flow.Flow
import kotlinx.coroutines.flow.map

private val Context.dataStore: DataStore<Preferences> by preferencesDataStore(name = "notifa_preferences")

class PreferencesManager(private val context: Context) {

    companion object {
        val ONBOARDING_COMPLETED = booleanPreferencesKey("onboarding_completed")
        val LAST_BATCH_TIME = longPreferencesKey("last_batch_time")
        val NOTIFICATION_PERMISSION_GRANTED =
            booleanPreferencesKey("notification_permission_granted")
        val USAGE_STATS_PERMISSION_GRANTED = booleanPreferencesKey("usage_stats_permission_granted")
    }

    val onboardingCompleted: Flow<Boolean> = context.dataStore.data.map { preferences ->
        preferences[ONBOARDING_COMPLETED] ?: false
    }

    suspend fun setOnboardingCompleted(completed: Boolean) {
        context.dataStore.edit { preferences ->
            preferences[ONBOARDING_COMPLETED] = completed
        }
    }

    val notificationPermissionGranted: Flow<Boolean> = context.dataStore.data.map { preferences ->
        preferences[NOTIFICATION_PERMISSION_GRANTED] ?: false
    }

    suspend fun setNotificationPermissionGranted(granted: Boolean) {
        context.dataStore.edit { preferences ->
            preferences[NOTIFICATION_PERMISSION_GRANTED] = granted
        }
    }

    val usageStatsPermissionGranted: Flow<Boolean> = context.dataStore.data.map { preferences ->
        preferences[USAGE_STATS_PERMISSION_GRANTED] ?: false
    }

    suspend fun setUsageStatsPermissionGranted(granted: Boolean) {
        context.dataStore.edit { preferences ->
            preferences[USAGE_STATS_PERMISSION_GRANTED] = granted
        }
    }
}

